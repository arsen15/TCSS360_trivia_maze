public class playerInMaze {
    private int myX;
    private int myY;
    
    public playerInMaze (int theX, int theY) {
      theX = myX;
      theY = myY;
    }
    
    public int getPlayerXCoordinate() {
      return myX;
    }
    
    public int getPlayerYCoordinate() {
      return myY;
    }
}
